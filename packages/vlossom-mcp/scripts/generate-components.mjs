#!/usr/bin/env node
// Generates src/data/components-data.ts from the vlossom components directory.
// Run before build: npm run generate

import { readdirSync, readFileSync, existsSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const componentsPath = resolve(__dirname, "../../vlossom/src/components");
const outputDir = resolve(__dirname, "../src/data");
const outputPath = join(outputDir, "components-data.ts");

if (!existsSync(componentsPath)) {
    console.error(`Components directory not found: ${componentsPath}`);
    process.exit(1);
}

function kebabToPascalCase(kebab) {
    return kebab
        .split("-")
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join("");
}

function readDescription(componentDir) {
    const readmePath = join(componentDir, "README.md");
    if (!existsSync(readmePath)) return "";
    const lines = readFileSync(readmePath, "utf-8").split("\n");
    return (
        lines
            .map((l) => l.trim())
            .find((l) => l.length > 0 && !l.startsWith(">") && !l.startsWith("#")) ?? ""
    );
}

const components = readdirSync(componentsPath, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && entry.name.startsWith("vs-"))
    .map((entry) => {
        const kebabName = entry.name;
        return {
            name: kebabToPascalCase(kebabName),
            kebabName,
            description: readDescription(join(componentsPath, kebabName)),
        };
    })
    .sort((a, b) => a.name.localeCompare(b.name));

mkdirSync(outputDir, { recursive: true });

const output = `// Auto-generated by scripts/generate-components.mjs — do not edit manually
import type { ComponentInfo } from "../services/component-registry.js";

export const BUNDLED_COMPONENTS: ComponentInfo[] = ${JSON.stringify(components, null, 4)};
`;

writeFileSync(outputPath, output, "utf-8");
console.log(`Generated ${components.length} components → ${outputPath}`);
